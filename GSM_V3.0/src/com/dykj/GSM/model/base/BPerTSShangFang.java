package com.dykj.GSM.model.base;

import org.hibernate.validator.constraints.NotBlank;

import com.dykj.GSM.model.BaseModel;

/**
 * @author 王海红
 * 
 * @description 特殊人群中的重点上访
 */
public class BPerTSShangFang extends BaseModel {
	/**
	 * 人口基本信息关联code
	 */
	private String personCode;

	/**
	 * 开始上访时间
	 */
	private String petitionDate;
	/**
	 * 问题管辖部门
	 */
	@NotBlank
	private String problemScope;
	/**
	 * 上访事由
	 */
	private String petitionReason;
	/**
	 * 处理结论
	 */
	private String handleConclusion;
	/**
	 * 受处理情况
	 */
	private String handleSituation;
	/**
	 * 稳控措施
	 */
	private String controlMeasures;
	/**
	 * 管控人员
	 */
	private String controlPerson;
	/**
	 * 管控人员电话
	 */
	private String controlPhone;

	public String getPersonCode() {
		return personCode;
	}

	public void setPersonCode(String personCode) {
		this.personCode = personCode;
	}

	public String getPetitionDate() {
		return petitionDate;
	}

	public void setPetitionDate(String petitionDate) {
		this.petitionDate = petitionDate;
	}

	public String getProblemScope() {
		return problemScope;
	}

	public void setProblemScope(String problemScope) {
		this.problemScope = problemScope;
	}

	public String getPetitionReason() {
		return petitionReason;
	}

	public void setPetitionReason(String petitionReason) {
		this.petitionReason = petitionReason;
	}

	public String getHandleConclusion() {
		return handleConclusion;
	}

	public void setHandleConclusion(String handleConclusion) {
		this.handleConclusion = handleConclusion;
	}

	public String getHandleSituation() {
		return handleSituation;
	}

	public void setHandleSituation(String handleSituation) {
		this.handleSituation = handleSituation;
	}

	public String getControlMeasures() {
		return controlMeasures;
	}

	public void setControlMeasures(String controlMeasures) {
		this.controlMeasures = controlMeasures;
	}

	public String getControlPerson() {
		return controlPerson;
	}

	public void setControlPerson(String controlPerson) {
		this.controlPerson = controlPerson;
	}

	public String getControlPhone() {
		return controlPhone;
	}

	public void setControlPhone(String controlPhone) {
		this.controlPhone = controlPhone;
	}

}
