package com.dykj.GSM.model.warningset;

import com.dykj.GSM.annotation.MapperUrlAnnotion;
import com.dykj.GSM.model.BaseModel;

/**
 * 预警提醒-提醒设置
 */
@MapperUrlAnnotion(mapperUrl = "com.dykj.GSM.mapper.warningset.mapperinterf.WarningSetMapper")
public class WarningSet extends BaseModel {

	/**
	 * 事件默认办理天数
	 */
	private Integer defaultBanLiDateEvent;
	/**
	 * 事件绿变黄时间
	 */
	private Integer eventToYellow;
	/**
	 * 事件默认办理天数
	 */
	private Integer defaultBanLiDateContradict;
	/**
	 * 矛盾绿变黄时间
	 */
	private Integer contradictToYellow;

	public Integer getDefaultBanLiDateEvent() {
		return defaultBanLiDateEvent;
	}

	public void setDefaultBanLiDateEvent(Integer defaultBanLiDateEvent) {
		this.defaultBanLiDateEvent = defaultBanLiDateEvent;
	}

	public Integer getEventToYellow() {
		return eventToYellow;
	}

	public void setEventToYellow(Integer eventToYellow) {
		this.eventToYellow = eventToYellow;
	}

	public Integer getDefaultBanLiDateContradict() {
		return defaultBanLiDateContradict;
	}

	public void setDefaultBanLiDateContradict(Integer defaultBanLiDateContradict) {
		this.defaultBanLiDateContradict = defaultBanLiDateContradict;
	}

	public Integer getContradictToYellow() {
		return contradictToYellow;
	}

	public void setContradictToYellow(Integer contradictToYellow) {
		this.contradictToYellow = contradictToYellow;
	}

}
