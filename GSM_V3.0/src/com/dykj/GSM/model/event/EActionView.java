package com.dykj.GSM.model.event;

/**
 * @author 王海红
 * 
 * @description 用于显示事件处理action的类，在数据库中没有此表
 */
public class EActionView {
	/**
	 * acation id
	 */
	private int id;
	/**
	 * action name
	 */
	private String name;
	/**
	 * 地址
	 */
	private String workUrl;
	/**
	 * 下一个步骤的权限
	 */
	private String stepRight;
	/**
	 * 是否出现评价选择框
	 */
	private Boolean apprise = false;
	/**
	 * 是否显示时限框
	 */
	private Boolean duration = false;
	/**
	 * 是否显示选择部门框
	 */
	private Boolean depart = false;
	/**
	 * 是否显示延长时限
	 */
	private Boolean delay = false;

	/**
	 * 在延时申请审批时，显示是否显示原来的时限
	 */
	private Boolean delayApply = false;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getWorkUrl() {
		return workUrl;
	}

	public void setWorkUrl(String workUrl) {
		this.workUrl = workUrl;
	}

	public String getStepRight() {
		return stepRight;
	}

	public void setStepRight(String stepRight) {
		this.stepRight = stepRight;
	}

	public Boolean getApprise() {
		return apprise;
	}

	public void setApprise(Boolean apprise) {
		this.apprise = apprise;
	}

	public Boolean getDuration() {
		return duration;
	}

	public void setDuration(Boolean duration) {
		this.duration = duration;
	}

	public Boolean getDepart() {
		return depart;
	}

	public void setDepart(Boolean depart) {
		this.depart = depart;
	}

	public Boolean getDelay() {
		return delay;
	}

	public void setDelay(Boolean delay) {
		this.delay = delay;
	}

	public Boolean getDelayApply() {
		return delayApply;
	}

	public void setDelayApply(Boolean delayApply) {
		this.delayApply = delayApply;
	}

}
