package com.dykj.GSM.model.emergencyscheduling;

import com.dykj.GSM.annotation.MapperUrlAnnotion;
import com.dykj.GSM.model.BaseModel;

@MapperUrlAnnotion(mapperUrl = "com.dykj.GSM.mapper.emergencyscheduling.mapperinterf.SysGpsPositionMapper")
public class SysGpsPosition extends BaseModel {

	/**
	 * 设备IMEI
	 */
	private String deviceImei;
	/**
	 * GPS定位时间
	 */
	private String gpsTime;
	/**
	 * 横坐标
	 */
	private String lon;
	/**
	 * 纵坐标
	 */
	private String lat;
	/**
	 * 速度
	 */
	private String speed;
	/**
	 * GPS类型
	 */
	private String gpsType;
	/**
	 * 时间段
	 */
	private String dateTime1;
	/**
	 * 时间段2
	 */
	private String dateTime2;

	public String getDeviceImei() {
		return deviceImei;
	}

	public void setDeviceImei(String deviceImei) {
		this.deviceImei = deviceImei;
	}

	public String getGpsTime() {
		return gpsTime;
	}

	public void setGpsTime(String gpsTime) {
		this.gpsTime = gpsTime;
	}

	public String getLon() {
		return lon;
	}

	public void setLon(String lon) {
		this.lon = lon;
	}

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getSpeed() {
		return speed;
	}

	public void setSpeed(String speed) {
		this.speed = speed;
	}

	public String getGpsType() {
		return gpsType;
	}

	public void setGpsType(String gpsType) {
		this.gpsType = gpsType;
	}

	public String getDateTime1() {
		return dateTime1;
	}

	public void setDateTime1(String dateTime1) {
		this.dateTime1 = dateTime1;
	}

	public String getDateTime2() {
		return dateTime2;
	}

	public void setDateTime2(String dateTime2) {
		this.dateTime2 = dateTime2;
	}

}
