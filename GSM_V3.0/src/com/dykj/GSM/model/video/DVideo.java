package com.dykj.GSM.model.video;

import javax.persistence.Transient;

import com.dykj.GSM.model.BaseModel;

/**
 * 视联网-视频管理
 * 
 */
public class DVideo extends BaseModel {
	
	
	/**
	 * 视频名称
	 */
	private String videoName;
	/**
	 * 视频地址
	 */
	private String videoAddress;
	/**
	 * 所属辖区或部门
	 */
	private String gridName;
	/**
	 * 编码
	 */
	private String codeVideonet;
	/**
	 * 类型
	 */
	private String typeVideoname;
	/**
	 * 通道
	 */
	private String codeChannel;
	/**
	 * 地图坐标
	 */
	private String position;
	public String getVideoName() {
		return videoName;
	}
	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}
	public String getVideoAddress() {
		return videoAddress;
	}
	public void setVideoAddress(String videoAddress) {
		this.videoAddress = videoAddress;
	}
	public String getGridName() {
		return gridName;
	}
	public void setGridName(String gridName) {
		this.gridName = gridName;
	}
	public String getCodeVideonet() {
		return codeVideonet;
	}
	public void setCodeVideonet(String codeVideonet) {
		this.codeVideonet = codeVideonet;
	}
	public String getTypeVideoname() {
		return typeVideoname;
	}
	public void setTypeVideoname(String typeVideoname) {
		this.typeVideoname = typeVideoname;
	}
	public String getCodeChannel() {
		return codeChannel;
	}
	public void setCodeChannel(String codeChannel) {
		this.codeChannel = codeChannel;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	
}
